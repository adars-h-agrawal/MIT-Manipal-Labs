Reset_Handler
	LDR R1,=0x255;
	AND R2,R1,#0x0F;
	MOV R3,#0;
	MOV R4,#0;
	MOV R6,#2;
	MOV R8,#0x0A;
LOOP
	ADD R2,R3;
	LSR R1,#4;
	AND R3,R1,#0x0F;
	ADD R4,#1;
	MOV R5,#0;
LOOP1
	MUL R3,R8;
	ADD R5,#1;
	TEQ R4,R5;
	BNE LOOP1
	SUBS R6,#1;
	BNE LOOP
	ADD R2,R3;
	LDR R7,=DST;
	STR R2,[R7];
STOP B STOP
