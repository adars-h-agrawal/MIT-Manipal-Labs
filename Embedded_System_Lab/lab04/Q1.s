	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=NUM;
	LDR R3,=DST;
	LDRB R1,[R0];
	AND R2,R1,#0x0F;
	CMP R2,#09;
	BLO DOWN;
	ADD R2,#07;
DOWN 
	ADD R2,#0x30;
	STRB R2,[R3],#01;
	AND R2,R1,#0xF0;
	LSR R2,#04;
	CMP R2,#09;
	BLO DOWN1
	ADD R2,#07;
DOWN1
	ADD R2,#0x30;
	STRB R2,[R3];
STOP B STOP
NUM DCD 0x3A;
	AREA mydata,DATA,READWRITE
DST DCD 0
	END