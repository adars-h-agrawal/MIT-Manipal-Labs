	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10000000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R1,=0x7;
	LDR R13,=0x10001000
	BL FACT_CAL
	LDR R1,=FACT;
	STR R2,[R1];
STOP B STOP

FACT_CAL
	CMP R1,#1;
	BEQ EXIT
	PUSH{R1}
	PUSH{LR}
	SUB R1,#1
	BL FACT_CAL
	POP{LR}
	POP{R1}
	MUL R2,R1
	BX LR
EXIT
	MOV R2,#1;
	BX LR
	AREA mydata,DATA,READWRITE
FACT DCD 0
	END